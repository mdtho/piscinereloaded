int	ft_sqrt(int nb)
{
	int x;

	if (nb < 0 || nb >= 2147483647)
		return (0);
	if (nb == 0 || nb == 2 || nb == 3 || nb == 5 || nb == 6 || nb == 7)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	if (nb == 9)
		return (3);
	if (nb == 16)
		return (4);
	if (nb == 25)
		return (5);
	x = 7;
	while (x <= nb / 5)
	{
		if (x * x == nb)
			return (x);
		x++;
	}
	return (0);
}
